package C.Operators;

public class Relational {
    public static void main(String[] args) {

        int a=50;
        int b = 40;

        System.out.println(a==b); //f
        System.out.println(a!=b); //t
        System.out.println(a>b); //t
        System.out.println(a<b); //f
        System.out.println(a>=b); //t
        System.out.println(a<=b); //f
    }


}
