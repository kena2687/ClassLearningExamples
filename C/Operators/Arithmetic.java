package C.Operators;

public class Arithmetic {
    public static void main(String[] args) {
        float a=10;
        float b = 20;

        System.out.println(a+b); //30
        System.out.println(a-b); //-10
        System.out.println(a*b); //200
        System.out.println(a/b); //0 , 0.5
        System.out.println(a%b); //0.2, 2 , 10

        }
}
