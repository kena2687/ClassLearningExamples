package C.Operators;

public class Unary {

    public static void main(String[] args) {
        int a=10;
        boolean b = true;

        System.out.println(a++); // 10
        System.out.println(++a); //12
        System.out.println(a--); // 12
        System.out.println(--a); // 10
        System.out.println(!b); //f

    }
}
